/*! \mainpage Integration layer for Pkcs11Interop and iText (iTextSharp) libraries
 *
 * <a href="https://github.com/itext/itextsharp">iTextSharp</a> is a C# port of iText - an open-source Java library for PDF generation and manipulation. It can be used to create PDF documents from scratch, to convert XML to PDF, to fill out interactive PDF forms, to stamp new content on existing PDF documents, to split and merge existing PDF documents, to add digital signatures to PDF documents and much more.
 * 
 * <a href="https://github.com/Pkcs11Interop/Pkcs11Interop">Pkcs11interop</a> is managed library written in C# that brings full power of PKCS#11 API to the .NET environment. PKCS#11 is cryptography standard maintained by the OASIS PKCS 11 Technical Committee (originally published by RSA Laboratories) that defines ANSI C API to access smart cards and other types of cryptographic hardware.
 *
 * <a href="https://github.com/jariq/Pkcs11Interop.PDF">Pkcs11interop.PDF</a> creates an integration layer between Pkcs11Interop and iTextSharp libraries by extending iTextSharp with the ability to digitally sign PDF document with the private key stored on almost any PKCS#11 compatible device.
 */


